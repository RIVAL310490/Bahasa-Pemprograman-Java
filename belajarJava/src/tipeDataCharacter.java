public class tipeDataCharacter {
    public static void main(String[] args) {

        /*
        Tipe Data Character
        * Data Character (huruf) di Java direpresentasikan oleh tipe char.
        * Untuk membuat data char di Java, kita bisa menggunakan tanda '(petik satu) di awal dan di akhir karakter
         */

        char r = 'r';
        char v = 'v';
        char l = 'l';

        System.out.print(r);
        System.out.print(v);
        System.out.print(l);

        char angka = 65;
        System.out.println("\n" + angka); //setiap karakter yang anda lihat memiliki nilai ASCII dibaliknya

    }
}
