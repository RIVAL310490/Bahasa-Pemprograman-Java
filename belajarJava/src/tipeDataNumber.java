public class tipeDataNumber {

    public static void main(String[] args) {

        /*
        INTEGER NUMBER
        byte (min: -128, max: 127, size: 1 byte)
        short (min: -32,768, max: 32,767, size: 2 bytes)
        int (min: -2,147,483,648, max: 2,147,483,647, size: 4 bytes)
        long (min: -9,223,372,036,854,775,808, max: 9,223,372,036,854,775,807 size: 8 bytes)
         */

        // kode integer number
        byte iniByte = 100;
        short iniShort = 1000;
        int iniInt = 100000000;
        long iniLong = 1000000000;
        long iniLong2 = 1000000000L;

        /*
        Floating Point Number
        float (min: 3.4e-038, max: 3.4e+038, size: 4 bytes)
        double (min: 1.7e-308, max: 1.7e+308, size: 8 bytes)
         */

        float iniFloat = 1000000000.0f;
        double iniDouble = 10000000000000.0000000;

        //Kode: Literals

        int decimalInt = 10;
        int hexadecimalInt = 0x1; // harus ditambahkan x
        int binaryDecimal = 0b1; // harus ditambahkan b

        //Kode: Underscore
        int jumlah = 1_000_000_000;

        //Konversi Tipe Data Number



    }
}
